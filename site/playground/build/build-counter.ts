/**
 * Build the playground's generated artifacts for the host Worker.
 *
 * The app is a MULTI-ROUTE Hono app: `template/server.tsx` declares page routes
 * that render barefoot components from `template/src/<Name>.tsx`. A Cloudflare
 * Dynamic Worker has no filesystem at runtime, so everything the host needs is
 * embedded as generated string constants.
 *
 * This script does, in order:
 *
 *   1. rt-counter.ts   — run compileApp({}) (the in-JS, no-CLI, no-Bun.build
 *      pipeline) over the default app and embed its Worker-Loader module set +
 *      the static asset bodies the host serves (barefoot.js, uno.css, one
 *      <Name>.client.js per component). This is what a fresh visitor previews.
 *   2. app-files.ts    — the app's source files (server.tsx + every src/*.tsx +
 *      renderer.tsx + barefoot.config.ts) embedded for the editable Code
 *      Explorer / file tree.
 *   3. types-bundle.ts — the .d.ts set Monaco loads via `addExtraLib`.
 *   4. compile-worker.ts — the browser compile worker (compileAppCore +
 *      @barefootjs/jsx + UnoCSS + esbuild-wasm) + the embedded barefoot.js
 *      runtime.
 *
 * Run from anywhere: `bun run site/playground/build/build-counter.ts`
 */

import { spawnSync } from 'node:child_process'
import { readFile, writeFile, mkdir, readdir, stat } from 'node:fs/promises'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { compileApp } from './compile-app'
import { buildCompileWorker } from './build-compile-worker'

const HERE = dirname(fileURLToPath(import.meta.url))
const PLAYGROUND = join(HERE, '..')
const TEMPLATE = join(PLAYGROUND, 'template')
const REPO_ROOT = join(PLAYGROUND, '..', '..')
const GENERATED = join(PLAYGROUND, 'generated')

function log(step: string) {
  console.log(`\n=== ${step} ===`)
}

await mkdir(GENERATED, { recursive: true })

// 0. registry-bundle.ts + tokens-bundle.ts — pre-compile the FIXED ui registry
//    components (Button, Card, …) and the design-token CSS. compile-app /
//    worker.ts import these generated modules, so they must exist before
//    step 1 runs. (Idempotent; safe to re-run.)
log('0/4 registry-bundle.ts + tokens-bundle.ts')
await import('./build-registry')

// 1. rt-counter.ts — the default app's Worker-Loader modules + host assets.
log('1/4 rt-counter.ts')
const result = await compileApp({})
const rtModule = `// Generated by build/build-counter.ts — do not edit by hand.
// Worker-Loader module set for the default multi-route app, produced by
// compileApp() (in-JS compile; NO bf-CLI subprocess, NO Bun.build). Object-form
// values ({ js }) are the pre-bundled fixed framework keyed by bare specifier.
// RT_COUNTER_ASSETS are the static asset bodies the HOST serves over HTTP
// (barefoot.js + uno.css + one client.js per component); they are NOT modules.
export interface RtAppAssets {
  barefootJs: string
  unoCss: string
  clientJs: Record<string, string>
}
export const RT_COUNTER_MAIN = ${JSON.stringify(result.mainModule)}
export const RT_COUNTER_MODULES: Record<string, string | { js: string }> = ${JSON.stringify(result.modules, null, 2)}
export const RT_COUNTER_ASSETS: RtAppAssets = ${JSON.stringify(result.assets, null, 2)}
`
await writeFile(join(GENERATED, 'rt-counter.ts'), rtModule)
console.log(
  `rt-counter.ts: ${Object.keys(result.modules).length} modules, ` +
    `${Object.keys(result.assets.clientJs).length} client assets`,
)

// 2. app-files.ts — embed the app's source files for the editable Code
//    Explorer. server.tsx + every src/*.tsx + renderer.tsx + barefoot.config.ts.
log('2/4 app-files.ts')

function langForPath(path: string): string {
  if (path.endsWith('.tsx') || path.endsWith('.ts')) return 'typescript'
  if (path.endsWith('.jsx') || path.endsWith('.js')) return 'javascript'
  if (path.endsWith('.css')) return 'css'
  if (path.endsWith('.json')) return 'json'
  return 'plaintext'
}

// server.tsx first (it's the entry the AI edits most), then src components,
// then the fixed scaffolding files.
const srcNames = (await readdir(join(TEMPLATE, 'src')))
  .filter((n) => n.endsWith('.tsx'))
  .sort()
const APP_FILE_PATHS = [
  'server.tsx',
  ...srcNames.map((n) => `src/${n}`),
  'renderer.tsx',
  'barefoot.config.ts',
]

const appFiles: Array<{ path: string; content: string; lang: string }> = []
for (const path of APP_FILE_PATHS) {
  const content = await readFile(join(TEMPLATE, path), 'utf8')
  appFiles.push({ path, content, lang: langForPath(path) })
}
const appFilesModule = `// Generated by build/build-counter.ts — do not edit by hand.
// The app's source files, embedded for the editable Code Explorer. Neither the
// host Worker nor the Dynamic Worker has a filesystem at runtime, so the
// explorer reads file content from this constant.
export interface AppFile {
  path: string
  content: string
  lang: string
}
export const APP_FILES: AppFile[] = ${JSON.stringify(appFiles, null, 2)}
`
await writeFile(join(GENERATED, 'app-files.ts'), appFilesModule)
console.log(`app-files.ts: ${appFiles.length} files`)

// 3. types-bundle.ts — the .d.ts set Monaco's TypeScript service loads via
//    `addExtraLib`, giving the editable Code Explorer real autocomplete and
//    accurate diagnostics against @barefootjs/hono/jsx (the JSX source) and
//    @barefootjs/client (the signals API).
log('3/4 types-bundle.ts')

const PKG_DIR = join(REPO_ROOT, 'packages')

const clientDtsFile = join(PKG_DIR, 'client', 'dist', 'index.d.ts')
async function fileExists(path: string): Promise<boolean> {
  try {
    await stat(path)
    return true
  } catch {
    return false
  }
}
if (!(await fileExists(clientDtsFile))) {
  console.log('Building @barefootjs/client declarations for playground types…')
  const dts = spawnSync('bun', ['run', 'build:types'], {
    cwd: join(PKG_DIR, 'client'),
    stdio: 'inherit',
  })
  if (dts.status !== 0 || !(await fileExists(clientDtsFile))) {
    throw new Error(
      'Failed to build @barefootjs/client declarations (dist/index.d.ts missing)',
    )
  }
}

const HONO_JSX_SHIM = `declare module 'hono/jsx' {
  export namespace JSX {
    type Element = unknown
  }
}
`
const HONO_JSX_RUNTIME_SHIM = `declare module 'hono/jsx/jsx-runtime' {
  type Props = Record<string, unknown>
  export function jsx(tag: string | Function, props: Props, key?: string): unknown
  export const jsxs: typeof jsx
  export function Fragment(props: { children?: unknown }): unknown
  export function jsxAttr(name: string, value: unknown): string
  export function jsxEscape(value: unknown): string
  export function jsxTemplate(
    strings: TemplateStringsArray,
    ...values: unknown[]
  ): unknown
}
`

// The server imports `hono` (Hono) and `hono/jsx-renderer` (jsxRenderer); shim
// just the symbols the playground app uses so the editor resolves them without
// pulling the full Hono type graph into Monaco's TS service.
const HONO_SHIM = `declare module 'hono' {
  export class Hono {
    get(path: string, handler: (c: any) => unknown): this
    post(path: string, handler: (c: any) => unknown): this
    use(...handlers: unknown[]): this
    route(path: string, app: Hono): this
  }
}
`
const HONO_JSX_RENDERER_SHIM = `declare module 'hono/jsx-renderer' {
  export function jsxRenderer(
    component: (props: { children?: unknown }) => unknown,
  ): (c: any, next: any) => unknown
  export function useRequestContext(): any
}
`

const HONO_SCRIPTS_SHIM = `declare module '@barefootjs/hono/scripts' {
  export interface BfScriptsProps {
    nonce?: string
    basePath?: string
  }
  export function BfScripts(props?: BfScriptsProps): unknown
}
`
const HONO_BUILD_SHIM = `declare module '@barefootjs/hono/build' {
  export interface HonoBuildOptions {
    components?: string[]
    outDir?: string
    minify?: boolean
    scriptBasePath?: string
    adapterOptions?: Record<string, unknown>
    [key: string]: unknown
  }
  export function createConfig(options?: HonoBuildOptions): HonoBuildOptions
}
`

const typeBundle: Record<string, string> = {
  'file:///node_modules/@barefootjs/hono/jsx/jsx-runtime/index.d.ts': await readFile(
    join(PKG_DIR, 'adapter-hono', 'src', 'jsx', 'jsx-runtime', 'index.ts'),
    'utf8',
  ),
  'file:///node_modules/@barefootjs/jsx/jsx-runtime/index.d.ts': await readFile(
    join(PKG_DIR, 'jsx', 'src', 'jsx-runtime', 'index.d.ts'),
    'utf8',
  ),
  'file:///node_modules/@barefootjs/jsx/html-types.d.ts': await readFile(
    join(PKG_DIR, 'jsx', 'src', 'html-types.ts'),
    'utf8',
  ),
  'file:///node_modules/@barefootjs/client/index.d.ts': await readFile(
    clientDtsFile,
    'utf8',
  ),
  'file:///node_modules/hono/index.d.ts': HONO_SHIM,
  'file:///node_modules/hono/jsx/index.d.ts': HONO_JSX_SHIM,
  'file:///node_modules/hono/jsx/jsx-runtime/index.d.ts': HONO_JSX_RUNTIME_SHIM,
  'file:///node_modules/hono/jsx-renderer/index.d.ts': HONO_JSX_RENDERER_SHIM,
  'file:///node_modules/@barefootjs/hono/scripts/index.d.ts': HONO_SCRIPTS_SHIM,
  'file:///node_modules/@barefootjs/hono/build/index.d.ts': HONO_BUILD_SHIM,
}

// UI registry ambient module declarations so the editor resolves the AI's
// `@/components/ui/<name>` imports (loose `any`-prop function components — the
// editor only needs the import to resolve, not precise prop types). Generated
// from the registry summary so it always matches the pre-compiled set.
{
  const { REGISTRY_SUMMARY } = await import('../generated/registry-bundle')
  for (const c of REGISTRY_SUMMARY) {
    const decls = c.exports
      .map((name) => `  export function ${name}(props?: any): any`)
      .join('\n')
    typeBundle[`file:///node_modules/${c.key}/index.d.ts`] =
      `declare module '${c.key}' {\n${decls}\n}\n`
  }
}

const typesBundleModule = `// Generated by build/build-counter.ts — do not edit by hand.
// The Monaco TypeScript .d.ts bundle for the editable Code Explorer. Each key
// is a virtual file:/// path passed to monaco's \`addExtraLib\`; the values are
// the BarefootJS typings (JSX + signals) so imports resolve and the editor
// reports accurate diagnostics. The host Worker has no filesystem at runtime,
// so it serves this constant verbatim at /_pg/types-bundle.json.
export const TYPES_BUNDLE: Record<string, string> = ${JSON.stringify(typeBundle, null, 2)}
`
await writeFile(join(GENERATED, 'types-bundle.ts'), typesBundleModule)
console.log(`types-bundle.ts: ${Object.keys(typeBundle).length} libs`)

// 4. compile-worker.ts — bundle the browser compile worker + embed barefoot.js.
log('4/4 compile-worker.ts')
await buildCompileWorker()

console.log('\nDone.')
