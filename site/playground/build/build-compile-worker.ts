/**
 * Bundle the browser compile worker (client/compile-worker.ts) into a single
 * browser-target ESM string and embed it as a generated constant the host
 * worker serves at /_pg/compile-worker.js.
 *
 * The worker bundles the isomorphic compileAppCore + @barefootjs/jsx +
 * @barefootjs/hono/build + UnoCSS + esbuild-wasm's JS shim (the .wasm binary
 * itself is fetched at runtime from a CDN, not bundled). The host can't read
 * files at runtime, so the bundle ships embedded as a string constant.
 *
 * Run: `bun run site/playground/build/build-compile-worker.ts`
 * (also invoked by build-counter.ts so a single build step refreshes it).
 */

import { writeFile, mkdir, readFile } from 'node:fs/promises'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = dirname(fileURLToPath(import.meta.url))
const PLAYGROUND = join(HERE, '..')
const GENERATED = join(PLAYGROUND, 'generated')
const REPO_ROOT = join(PLAYGROUND, '..', '..')

export async function buildCompileWorker(): Promise<void> {
  const result = await Bun.build({
    entrypoints: [join(PLAYGROUND, 'client', 'compile-worker.ts')],
    target: 'browser',
    format: 'esm',
    minify: true,
    jsx: {
      runtime: 'automatic',
      importSource: 'hono/jsx',
    } as unknown as undefined,
  })
  if (!result.success) {
    for (const m of result.logs) console.error(m)
    throw new Error('compile-worker Bun.build failed')
  }
  const workerJs = await result.outputs[0].text()
  console.log(`compile-worker.js: ${workerJs.length} bytes`)

  // The fixed barefoot.js DOM runtime, embedded so the host can serve it to the
  // browser worker at /_pg/barefoot-runtime.js (the worker fetches it to bake
  // the inline _assets.js module). Same artifact bf build copies to barefoot.js.
  const barefootRuntime = await readFile(
    join(REPO_ROOT, 'packages', 'client', 'dist', 'runtime', 'standalone.js'),
    'utf8',
  )
  console.log(`barefoot runtime: ${barefootRuntime.length} bytes`)

  await mkdir(GENERATED, { recursive: true })
  const module = `// Generated by build/build-compile-worker.ts — do not edit by hand.
// The browser compile worker (compileAppCore + @barefootjs/jsx + UnoCSS +
// esbuild-wasm), bundled for the browser and embedded as a string the host
// serves at /_pg/compile-worker.js. BAREFOOT_RUNTIME_JS is the fixed DOM
// runtime the worker fetches from /_pg/barefoot-runtime.js.
export const COMPILE_WORKER_JS = ${JSON.stringify(workerJs)}
export const BAREFOOT_RUNTIME_JS = ${JSON.stringify(barefootRuntime)}
`
  await writeFile(join(GENERATED, 'compile-worker.ts'), module)
  console.log('Wrote generated/compile-worker.ts')
}

// Allow running standalone.
if (import.meta.main) {
  await buildCompileWorker()
}
