/**
 * Spike harness: run compileApp() (the in-JS, no-CLI, no-Bun.build pipeline)
 * over the default Counter app and embed its Worker-Loader module set as a
 * generated constant the host worker loads at `/__rt-counter`.
 *
 * Why embed rather than call compileApp() inside the host worker: compileApp's
 * JSX transpile currently uses esbuild's NATIVE `transform` (Node addon) and
 * reads fixed assets via node:fs — neither runs inside workerd. The compilation
 * itself is in-JS with NO bf-CLI subprocess and NO Bun.build (the spike's bar).
 * The path to running fully inside the Worker is swapping esbuild→esbuild-wasm
 * and embedding the fixed assets as constants (see compile-app.ts header).
 *
 * Run: `bun run site/playground/build/build-rt-counter.ts`
 */

import { writeFile, mkdir } from 'node:fs/promises'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { compileApp } from './compile-app'

const HERE = dirname(fileURLToPath(import.meta.url))
const GENERATED = join(HERE, '..', 'generated')

const result = await compileApp({})

await mkdir(GENERATED, { recursive: true })
const module = `// Generated by build/build-rt-counter.ts — do not edit by hand.
// Worker-Loader module set for the default multi-route app, produced by
// compileApp() (in-JS compile; NO bf-CLI subprocess, NO Bun.build). Object-form
// values ({ js }) are the pre-bundled fixed framework keyed by bare specifier.
// RT_COUNTER_ASSETS are the static asset bodies the HOST serves over HTTP
// (barefoot.js + uno.css + one client.js per component); they are NOT modules.
export interface RtAppAssets {
  barefootJs: string
  unoCss: string
  clientJs: Record<string, string>
}
export const RT_COUNTER_MAIN = ${JSON.stringify(result.mainModule)}
export const RT_COUNTER_MODULES: Record<string, string | { js: string }> = ${JSON.stringify(result.modules, null, 2)}
export const RT_COUNTER_ASSETS: RtAppAssets = ${JSON.stringify(result.assets, null, 2)}
`
await writeFile(join(GENERATED, 'rt-counter.ts'), module)
console.log(
  `Wrote generated/rt-counter.ts: ${Object.keys(result.modules).length} modules, ` +
    `${Object.keys(result.assets.clientJs).length} client assets, main=${result.mainModule}`,
)
