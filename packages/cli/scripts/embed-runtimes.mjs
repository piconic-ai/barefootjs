#!/usr/bin/env node
// Embed adapter runtime files (Go / Perl) into a TS module of string
// constants so the bundled CLI can scaffold a self-contained starter
// without runtime file lookups against the source repo.
//
// Run before `bun build` (the package's `build` script chains both).
//
// **Workspace-only.** This script reads source files via repo-relative
// paths (`packages/adapter-*/...`), so it only works from inside the
// monorepo checkout. Consumers of a published `@barefootjs/cli`
// tarball never run it — the generated `runtimes.generated.ts` is
// already inlined into `dist/index.js` at publish time.

import { existsSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const cliPkg = resolve(here, '..')
const repoRoot = resolve(cliPkg, '../..')

// We deliberately don't embed the upstream `go.mod` files — the
// scaffold rewrites them to relax the Go version pin. Adapter source
// files (Go / Perl) are embedded as-is.
const sources = {
  bfGoSource: 'packages/adapter-go-template/runtime/bf.go',
  streamingGoSource: 'packages/adapter-go-template/runtime/streaming.go',
  barefootPmSource: 'packages/adapter-mojolicious/lib/BarefootJS.pm',
  barefootPluginPmSource: 'packages/adapter-mojolicious/lib/Mojolicious/Plugin/BarefootJS.pm',
}

// Pre-flight: surface a clear error when a source file is missing
// (incomplete checkout, recent rename, etc.) instead of throwing a
// raw ENOENT mid-loop with a confusing stack.
const missing = []
for (const [, relPath] of Object.entries(sources)) {
  const absPath = resolve(repoRoot, relPath)
  if (!existsSync(absPath)) missing.push(relPath)
}
if (missing.length > 0) {
  console.error('embed-runtimes: source file(s) missing — run from a complete monorepo checkout:')
  for (const p of missing) console.error(`  - ${p}`)
  process.exit(1)
}

let out = `// Auto-generated by scripts/embed-runtimes.mjs — do not edit by hand.
// Re-run \`bun run build\` (or \`bun run embed-runtimes\`) to refresh after
// editing the adapter runtime sources.

`

for (const [name, relPath] of Object.entries(sources)) {
  const absPath = resolve(repoRoot, relPath)
  const content = readFileSync(absPath, 'utf-8')
  // JSON.stringify safely escapes every character (backticks, ${, etc.)
  // and produces a valid JS string literal we can drop into source.
  out += `export const ${name} = ${JSON.stringify(content)}\n\n`
}

const target = resolve(cliPkg, 'src/lib/adapters/runtimes.generated.ts')
mkdirSync(dirname(target), { recursive: true })

// Skip the write when content is identical so `bun --watch` and
// incremental dev loops don't see a spurious change every build.
const prev = existsSync(target) ? readFileSync(target, 'utf-8') : null
if (prev === out) {
  console.log(`Embedded ${Object.keys(sources).length} runtime files (unchanged) → ${target}`)
} else {
  writeFileSync(target, out)
  console.log(`Embedded ${Object.keys(sources).length} runtime files → ${target}`)
}
