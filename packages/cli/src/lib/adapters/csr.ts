// Client-side rendering (CSR) adapter starter.
//
// Ships a static HTML page with an empty mount point and a tiny
// `node:http` server that serves the page + the compiled client
// bundles produced by `bf build`. The `createConfig` from
// `@barefootjs/client/build` switches the compiler into CSR mode — no
// SSR — everything renders in the browser via @barefootjs/client/runtime.
//
// The server is plain Node (run via `tsx`, same as the hono-node
// starter) so a user on npm / pnpm / yarn isn't forced to install Bun.
// It still runs unchanged under Bun or Deno — `node:http` + `node:fs`
// are supported everywhere.

